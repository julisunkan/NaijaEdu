### Understanding the Web

To build effective web applications, you need to understand how the web works:

**Client-Server Architecture:**
The web operates on a client-server model where browsers (clients) request resources from web servers. This communication happens through HTTP (HyperText Transfer Protocol).

**Domain Names and Hosting:**
Every website needs a domain name (like example.com) and hosting (server space). In Nigeria, popular hosting providers include:
- Whogohost
- Smart Web Nigeria
- Web4Africa
- Qservers

**Web Standards:**
The web is built on open standards maintained by organizations like:
- W3C (World Wide Web Consortium)
- WHATWG (Web Hypertext Application Technology Working Group)
- ECMA International (JavaScript standards)

